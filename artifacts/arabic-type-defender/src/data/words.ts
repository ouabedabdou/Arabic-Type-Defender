export function getWordsByDifficulty(difficulty: Difficulty): string[] {
  switch (difficulty) {
    case "beginner":
      return WORDS_EASY;
    case "intermediate":
      return [...WORDS_EASY, ...WORDS_MEDIUM];
    case "advanced":
      return [...WORDS_MEDIUM, ...WORDS_HARD];
    case "survival":
      return [...WORDS_EASY, ...WORDS_MEDIUM, ...WORDS_HARD];
    default:
      return WORDS_EASY;
  }
}

export function getRandomWord(words: string[]): string {
  return words[Math.floor(Math.random() * words.length)];
}

export function getWordDifficulty(word: string): "easy" | "medium" | "hard" {
  if (word.length <= 4) return "easy";
  if (word.length <= 7) return "medium";
  return "hard";
}
